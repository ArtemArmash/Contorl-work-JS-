let name_heroes = prompt("Введіть ім'я вашого героя:");
let choice;
let inventory = [];
let experience = 0;

console.log(`${name_heroes} заходить в бар на "Скадовську" і підходить до Бороди.`);
console.log("Борода: Привіт, сталкер! Чим можу допомогти?");

do {
    console.log("\nВиберіть варіант:");
    console.log("1. Я тут новенький. Розкажи, що до чого?");
    console.log("2. Що тут цікавого відбувається?");
    console.log("3. Які завдання ти можеш запропонувати?");
    console.log("4. Перевірити інвентар");
    console.log("5. Вийти");

    choice = parseInt(prompt("Вибирайте з вищепереліченого"));

    switch (choice) {
        case 1:
            console.log(`\n${name_heroes}: Я тут новенький. Розкажи, що до чого?`);
            console.log("Борода: Добре, слухай. Я Борода, бармен і інформатор на 'Скадовську'. Це наш притулок, де можна відпочити, поповнити запаси і дізнатися останні новини. Якщо тобі потрібна робота або інформація, звертайся до мене.");
            break;
        case 2:
            console.log(`\n${name_heroes}: Що тут цікавого відбувається?`);
            console.log("Борода: Роботи вистачає. Тут неподалік активність мутантів збільшилася, аномалії теж не дають спокою. Якщо цікаво, можеш спробувати себе в ролі мисливця за артефактами або взятися за інші завдання. Отримуєш плату та можливість обмінювати знайдені артефакти на корисні речі.");
            break;ы
        case 3:
            console.log(`\n${name_heroes}: Які завдання ти можеш запропонувати?`);
            console.log("Борода: Є кілька варіантів. Можеш почати з простого: розібратися з мутантами, що занадто близько підходять до нашого притулку. Або пошукати артефакти в аномаліях. Вибирай, що більше до душі.");
            
            console.log("\nВиберіть завдання:");
            console.log("1. Знищити мутантів");
            console.log("2. Пошукати артефакти");
            let taskChoice = parseInt(prompt("Вибирайте завдання:"));

            if (taskChoice === 1) {
                console.log(`\n${name_heroes}: Я візьмусь за знищення мутантів.`);
                console.log("Борода: Добре, будь обережний там. Звітуй мені після виконання завдання.");
                
                console.log(`\n${name_heroes} виходить з бару і йде до місця, де помітили мутантів...`);
                console.log("Ти стикаєшся з групою псевдопсів. Після важкої битви ти перемагаєш їх.");
                inventory.push("Коготь псевдопса");
                experience += 10;
                console.log(`\n${name_heroes} повертається до Бороди.`);
                console.log("Борода: Ти впорався? Молодець! Ось твоя нагорода.");
                console.log(`\n${name_heroes} отримує 1000 ua.`);
                inventory.push("1000 ua");

                // Додаткові завдання після першого успішного
                console.log("\nБорода: Є ще одне завдання для тебе. Група сталкерів не повернулася з розвідки. Можеш знайти їх?");
                let rescueMission = parseInt(prompt("1. Так\n2. Ні"));
                if (rescueMission === 1) {
                    console.log(`\n${name_heroes}: Я знайду їх.`);
                    console.log("Борода: Сподіваюсь на тебе. Останній раз їх бачили біля старої фабрики.");
                    
                    console.log(`\n${name_heroes} йде до старої фабрики...`);
                    console.log("Після пошуків ти знаходиш зниклих сталкерів, що були атаковані мутантами. Ви допомагаєте їм відбитися і повертаєтесь на 'Скадовськ'.");
                    inventory.push("Подяка сталкерів");
                    experience += 20;
                    console.log(`\n${name_heroes} повертається до Бороди з врятованими сталкерами.`);
                    console.log("Борода: Чудова робота! Ось твоя нагорода.");
                    console.log(`\n${name_heroes} отримує 2000 ua і рідкісний артефакт.`);
                    inventory.push("2000 ua");
                    inventory.push("Рідкісний артефакт");
                } else {
                    console.log("\nБорода: Ну що ж, можливо наступного разу.");
                }
                
            } else if (taskChoice === 2) {
                console.log(`\n${name_heroes}: Я спробую знайти артефакти.`);
                console.log("Борода: Будь обережний у аномаліях. Повертайся, коли знайдеш щось цікаве.");
                
                console.log(`\n${name_heroes} виходить з бару і йде до найближчої аномалії...`);
                console.log("Ти знаходиш артефакт у центрі аномалії. Після обережного маневрування ти успішно його забираєш.");
                inventory.push("Артефакт");
                experience += 15;
                console.log(`\n${name_heroes} повертається до Бороди.`);
                console.log("Борода: Що ти знайшов? Вау, це справжній артефакт! Ось твоя нагорода.");
                console.log(`\n${name_heroes} отримує 1500 ua.`);
                inventory.push("1500 ua");

                // Додаткове завдання після першого успішного
                console.log("\nБорода: У мене є ще одне завдання для тебе. Є чутки про дуже рідкісний артефакт у новій аномалії. Хочеш спробувати знайти його?");
                let rareArtifactMission = parseInt(prompt("1. Так\n2. Ні"));
                if (rareArtifactMission === 1) {
                    console.log(`\n${name_heroes}: Я знайду цей рідкісний артефакт.`);
                    console.log("Борода: Удачі тобі. Аномалія знаходиться у дуже небезпечному районі.");
                    
                    console.log(`\n${name_heroes} йде до нової аномалії...`);
                    console.log("Ти знаходиш рідкісний артефакт, але аномалія дуже небезпечна. Після важких випробувань ти успішно забираєш артефакт.");
                    inventory.push("Рідкісний артефакт");
                    experience += 30;
                    console.log(`\n${name_heroes} повертається до Бороди.`);
                    console.log("Борода: Це неймовірно! Ось твоя щедра нагорода.");
                    console.log(`\n${name_heroes} отримує 5000 ua і унікальний предмет.`);
                    inventory.push("5000 рублів");
                    inventory.push("Унікальний предмет");
                } else {
                    console.log("\nБорода: Розумію, це дуже небезпечно.");
                }
            } else {
                console.log("Невірний вибір, спробуйте ще раз.");
            }
            break;
        case 4:
            console.log(`\n${name_heroes} перевіряє інвентар:`);
            console.log(inventory.length > 0 ? inventory.join(", ") : "Інвентар порожній.");
            break;
        case 5:
            console.log(`\n${name_heroes}: Ну то ладно, я пішов.`);
            console.log("Борода: До зустрічі.");
            break;
        default:
            console.log("\nНевірний вибір, спробуйте ще раз.");
            break;
    }
} while (choice !== 5);

console.log("\nГра завершена. Дякуємо за гру!");
